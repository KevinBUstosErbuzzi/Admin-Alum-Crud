package javaBase;

public class tiposDeDatos {

	public static void main(String[] args) {
		//declara la variable num como entero//
		int num;
		//numeros con comas o fracciones//
		double decimal;
		//texto//
		String letras;
		//logico//
		boolean condicional;
		
		//asigna el valor 12 a la variable//
		num=12;
		
		//muestro por consola el valor de la variable//
		System.out.println(num);
		
		num=32;
		System.out.println(num);
		
		decimal=2.5;
		System.out.println(decimal);
		
		letras="Juan Carlos";
		System.out.println(letras);
		
		condicional=true;
		System.out.println(condicional);
	}

}
